namespace Cratis.Applications.Queries;

/// <summary>
/// Defines the direction of sorting.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Unspecified sort direction.
    /// </summary>
    Unspecified = 0,

    /// <summary>
    /// Sort in ascending order.
    /// </summary>
    Ascending = 1,

    /// <summary>
    /// Sort in descending order.
    /// </summary>
    Descending = 2
}
