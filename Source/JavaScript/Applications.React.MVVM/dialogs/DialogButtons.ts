/**
 * Defines the buttons that can be shown on a standard dialog.
 */
export enum DialogButtons {

    /**
     * Show an OK button.
     */
    Ok = 1,

    /**
     * Show OK and Cancel buttons.
     */
    OkCancel = 2,

    /**
     * Show Yes and No buttons.
     */
    YesNo = 3,

    /**
     * Show Yes, No and Cancel buttons.
     */
    YesNoCancel = 4
}
