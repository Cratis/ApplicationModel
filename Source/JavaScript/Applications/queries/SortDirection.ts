/**
 * Defines the direction of sorting.
 */
export enum SortDirection {
    /**
     * Unspecified sort direction.
     */
    unspecified = 0,

    /**
     * Sort in ascending order.
     */
    ascending = 1,

    /**
     * Sort in descending order.
     */
    descending = 2,
}